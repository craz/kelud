<h3>Гарантия</h3>
<p>
	 На все товары бренда Kelud действует гарантия в течение 1 года с момента покупки и подписания передаточного документа.
</p>
<h3>Наши принципы</h3>
<p>
	 Для нас важно, чтобы вы остались довольны покупкой. В случае, если все требования к возврату, описанные ниже, соблюдены, мы обязательно вернем вам денежные средства за оплаченный товар или поменяем на аналогичный товар другого артикула.
</p>
<h3>Общие рекомендации по приемке товара</h3>
<ul>
	<li>Внимательно осмотрите товар при доставке, чтобы убедиться, что вас устраивает качество, фактура и размер предмета. </li>
	<li>В случае обнаружения повреждений или несоответствий заполните акт (сделайте пометку в накладной) до отъезда курьера. </li>
	<li>Сфотографируйте обнаруженные повреждения</li>
</ul>
<h3>Возврат товара ненадлежащего качества</h3>
<p>
	 Если при получении заказа был обнаружен брак или поставлен не тот товар, при условии, что недостаток возник не по вине клиента – замена и возврат заказа производится бесплатно в течение 14 дней при оповещении представителей KELUD в течение 24 часов, после получения товара.
</p>
<h3>Возврат товара надлежащего качества</h3>
<ul>
	<li><span>Если товар вам не подошел, вы можете его вернуть в течение 14 дней с даты получения, при условии доставки товара до склада Продавца за свой счет. (<a href="/upload/Товары, не подлежащие обмену и возврату.pdf" target="_blank">за исключением товаров, не подлежащих возврату, указанных в настоящих правилах</a>).</span></li>
	<li>В случае, если вы отказываетесь от товара после доставки, вы можете самостоятельно организовать доставку товара до нашего склада, или наша служба логистики организует обратную доставку для вас согласно действующим тарифам доставки, стоимость которых можно уточнить, связавшись с нашим менеджером.</li>
	<li>Возврат товара надлежащего качества возможен в случае, если сохранены его товарный вид (упаковка, пломбы, ярлыки), потребительские свойства, а также документ, подтверждающий факт и условия покупки указанного товара (товарный или кассовый чек), товарная накладная. </li>
	<li>Если товар состоит из нескольких частей, не забудьте вернуть все составляющие. Мы примем к возврату только полный комплект. </li>
	<li>После поступления товара на наш склад в г. Самара, расположенный по адресу: ул. 22 Партсъезда 7а корп.1, наши специалисты проверят состояние и качество возвращенного товара. По результатам проверки, будет принято решение по возврату денежных средств. </li>
	<li><span>Для возврата денежных средств, необходимо заполнить заявление (<a href="/upload/Заявление на возврат KELUD.docx">прилагается</a>). К заявлению прикладываются документы, подтверждающие факт и условия покупки (товарная накладная, кассовый/товарный чек). Денежные средства за возвращенный товар перечисляются безналичным расчетом на банковскую карту или расчетный счет покупателя, указанные в заявлении (за правильность указанных покупателем банковских реквизитов продавец ответственности не несет, платеж совершенный в соответствии с указанными реквизитами будет считаться засчитанным. Покупатель не вправе заявлять повторную претензию).</span></li>
	<li>Срок возврата денежных средств может занимать до рабочих 10 дней с момента поступления на склад и аннуляции возврата. Как правило, это занимает не более 7 рабочих дней.</li>
</ul>
