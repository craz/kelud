	<li>Товар надлежащего качества (без доказательств его повреждения при транспортировке), если не сохранены его товарный вид (упаковка, пломбы, ярлыки), потребительские свойства, а также документы, подтверждающие факт и условия покупки указанного товара (товарная накладная, товарный или кассовый чек).</li>
</ul>
